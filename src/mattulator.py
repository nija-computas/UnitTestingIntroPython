class Mattulator:
    def add(num1: float, num2: float) -> float:
        return num1 + num2

    def subtract(num1: float, num2: float) -> float:
        return num1 - num2

    def multiply(num1: float, num2: float) -> float:
        return num1 * num2

    def divide(num: float, denom: float) -> float:
        return denom / num
